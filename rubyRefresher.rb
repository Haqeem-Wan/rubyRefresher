    # Input
        ### Can be combined with Topic 3 to convert data types accordingly


        # Syntax :
            # variable = gets
            # variable = gets.to_i  # See Topic 3 for explanation on "to_i"


    # Method 1 : print

        ### using print will NOT add newline characters at the end of each print

        # Syntax :
            # print "This will be printed to the terminal! => "
            # print "This won't be sent to the next line!\n"

    # Method 2 : puts

        ### using puts WILL add newline characters at the end of each print

        # Syntax :
            # puts "This will be printed to the terminal!"
            # puts "This WILL be sent to the next line!"

