# Topic 2 : Variables!

    ### Defining variables in Ruby do NOT need to be preceeded by a Data Type!
    ### Ruby will instead assign the data type itself following the input given to the variable
    ### Because of this, variable initialization does not exist in Ruby, it is only variable declaration
    ### Convention is to start with a lower case letter, and use '-' or '_' between words

    # Syntax :
        # variable_1 = "String"     # Data Type assigned : string
        # variable_2 = 123          # Data Type assigned : integer
        # variable_3 = 'h'          # Data Type assigned : character
        # variable_4 = 1.23         # Data Type assigned : float

